        public void Setup()
        {
            // »нициализаци€ WebDriver и открытие страницы калькул€тора
            driver = new ChromeDriver();
            driver.Navigate().GoToUrl("https://www.globalsqa.com/angularJs-protractor/SimpleCalculator/");
